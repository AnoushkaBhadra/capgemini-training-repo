package literals_data;

public class LiteralsProg1 {
	public static void main(String[] args) {
		
		//Number Literals
		System.out.println(100);
		System.out.println(13.6);
		System.out.println(012); //Octal 
		System.out.println(0x231); //Hexadecimal
		System.out.println(0b011010000); //Binary
		
		//Character Literals
		System.out.println('A');
		System.out.println('@');
		
		//String Literals
		System.out.println("string literals");
		
		//Boolean Literals
		System.out.println(false);
		System.out.println(true);
		
		
		//Single Line Comment
		
		/* This is a multi-line
		 * Comment in Java */
	
		
		

	}
}
